uint8_t address_getConfigParamNr(const char *string)
{
	uint8_t result = MPE_ADDRESS_ERROR;
	for (uint8_t i = 0; i < ConfigParam_NumberOfParam; i++)
	{
		if (!strncmp(string, MPe_address_configParams[i], MPE_MAX_ADDRESS_LENGTH))
		{
			result = i;
			break;
		}
	}
	return result;
}
